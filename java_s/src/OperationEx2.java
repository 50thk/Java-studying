public class OperationEx2 {

    public static void main(String[] args) {
        /*  증가 감소 연산자 ++, --
            val = ++num; 증가감소연산자 -> val에 대입 연산자
            val = num++; 대입 연산자 이후 num값 증가감소연산자
         */
        int num = 10;
        System.out.println(++num);
        System.out.println(num++);
        System.out.println(num);

        boolean flag = (3 > 5);
        System.out.println(flag);

        /*
        논리연산자 %% = and, || = or, | = not
         */
    }
}
